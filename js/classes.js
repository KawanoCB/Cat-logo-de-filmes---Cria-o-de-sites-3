class Ator 
{
    constructor(id, nome){
        this.nome=nome;
        this.id=id;
    }
}

class Diretor {
    constructor(id, nome){
        this.nome=nome;
        this.id=id;
    }
}

class Filme {
    constructor(id, titulo, ano, genero, categoria, cartaz, direcao, atores, classificacao, avaliacao){
        this.id=id;
        this.titulo=titulo;
        this.ano=ano;
        this.genero=genero;
        this.categoria=categoria;
        this.cartaz=cartaz;
        this.direcao=direcao;
        this.atores.atores;
        this.classificacao=classificacao;
        this.avaliacao=avaliacao;
    }
}